    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Integer code = (Integer) catalogueClient.getValueAt(jTable2.getSelectedRow(), 0);
        try {
            clientDao.debloque(code);
            catalogueClient.updateClient(code);
        } catch (SQLException ex) {
            Logger.getLogger(CatalogueFournisseur.class.getName()).log(Level.SEVERE, null, ex);
        }
        catalogueClient.fireTableCellUpdated(jTable1.getSelectedRow(), 6);

    }//GEN-LAST:event_jButton2ActionPerformed

    public String construireRequette() {
        String requette = "SELECT * FROM client ";
        String bloque = jComboBox1.getSelectedItem() + "";
        if (bloque.equals("Bloque")) {
            requette += " WHERE statut='Bloque'";
        } else if (bloque.equals("Actif")) {
            requette += " WHERE statut='Actif'";
        }


        return requette;
    }

    
