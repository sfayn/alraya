    private double calculerSousTotal(){
        double sousTotal=0;
        List<CommandeProduit> commandeProduits = commandeViewSteep3Helper.getCommandeProduits();
        for (CommandeProduit commandeProduit : commandeProduits) {
            sousTotal+=commandeProduit.getMontant()*commandeProduit.getQuantite();
        }
        return sousTotal;
    }
