    private String construireRequet(){

        String requette = "SELECT * FROM suivi where ";
        String mois=jComboBox2.getSelectedItem()+"";
        int annnee=jYearChooser1.getValue();
        String bloque=jComboBox4.getSelectedItem()+"";
        String interesse= mySplit(jComboBox3.getSelectedItem()+"",0);
        String clauseDate = "'%-";
        if(!mois.equals("Tout")){
            clauseDate = clauseDate+ mois+"-";
        }
        else{
            clauseDate = clauseDate+ "%-";
        }
        clauseDate = clauseDate + annnee+"'";

        requette=requette + "dateSuivi like "+clauseDate ;
        if(bloque.equals("Oui"))
          requette=requette+" and bloque=1 ";
        else  if(bloque.equals("Non"))
             requette=requette+" and bloque=0 ";


        if((jComboBox1.getSelectedItem()+"").equals("Client")){
            requette = requette+" and type ='C'" ;
        }
        else if((jComboBox1.getSelectedItem()+"").equals("Fournisseur")){
            requette = requette+" and type ='F'" ;
        }

        if(!interesse.equals("Tout"))
        requette=requette+" and interesse ='"+interesse+"'";
        System.out.println(requette);
        return requette;
    }
